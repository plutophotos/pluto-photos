/**
 * i18n composable — call useI18n() in any component
 * Returns a `t(key)` function that looks up translations
 */
export function useI18n() {
  const t = (key) => {
    const lang = translations[currentLanguage.value]
    if (lang && lang[key]) return lang[key]
    // Fallback to English
    return translations.en[key] || key
  }

  const setLanguage = (lang) => {
    if (translations[lang]) {
      currentLanguage.value = lang
    }
  }

  return { t, currentLanguage, setLanguage }
}

/**
 * Load the saved language from settings (call once at app startup)
 */
export async function initLanguage() {
  try {
    const lang = await window.electron.ipcRenderer.invoke('get-setting', 'language')
    if (lang && translations[lang]) {
      currentLanguage.value = lang
    }
  } catch {}
}
